function showAddSummerList() {
	$("#add_summer_list").removeClass("hidden");
	$("#btn_hide_summer_list").removeClass("hidden");
	$("#btn_add_summer_list").addClass("hidden");
}

function hideAddSummerList() {
	$("#add_summer_list").addClass("hidden");
	$("#btn_add_summer_list").removeClass("hidden");
	$("#btn_hide_summer_list").addClass("hidden");
}
